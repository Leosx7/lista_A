numero1 = int(input('Digite o numero 1 : '))
numero2 = int(input('Digite o numero 2 : '))

soma = numero1 + numero2
subtracao = numero1 - numero2
divisao_soma_pela_subtracao = soma // subtracao

print(f'A soma dos dois numeros é : {soma}')
print(f'A subtração dos dois numeros é : {subtracao}')
print(f'A divisão da soma e subtração dos dois numeros é : {divisao_soma_pela_subtracao}')
